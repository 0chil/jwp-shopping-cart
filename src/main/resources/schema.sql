CREATE TABLE PRODUCT
(
    id INT NOT NULL AUTO_INCREMENT,
    name VARCHAR(200) NOT NULL,
    image VARCHAR(2047) NOT NULL,
    price BIGINT NOT NULL,
    PRIMARY KEY (id)
);

CREATE TABLE USERS
(
    id       INT          NOT NULL AUTO_INCREMENT,
    email    VARCHAR(200) NOT NULL,
    password VARCHAR(200) NOT NULL,
    PRIMARY KEY (id)
);